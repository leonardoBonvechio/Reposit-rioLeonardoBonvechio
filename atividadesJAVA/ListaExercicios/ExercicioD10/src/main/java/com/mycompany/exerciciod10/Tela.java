    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        float capital = Float.valueOf(txtCapital.getText());
        float meses   = Float.valueOf(txtMeses.getText());
        
        float totalJuros  = 0;
        float ultimoJuros = 0;
        for(int cont=0; cont < meses; cont++){
            ultimoJuros = (capital * 0.1f);
            totalJuros  = totalJuros + ultimoJuros;
            capital     = capital + ultimoJuros;
         }
        
        JOptionPane.showMessageDialog(rootPane, 
                "Total de juros: " + totalJuros);
    }//GEN-LAST:event_jButton1ActionPerformed

